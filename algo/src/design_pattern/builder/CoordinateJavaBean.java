package design_pattern.builder;

public class CoordinateJavaBean {

   //java bean pattern
	private int x=0;
	private int y=0;
	private int w=0;
	private int h=0;
	
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y) {
		this.y = y;
	}
	public void setW(int w) {
		this.w = w;
	}
	public void setH(int h) {
		this.h = h;
	}
	
}
